    <p>
        Bienvenue sur la plateforme de gestion du tutorat en alternance FSI, un espace digital conçu pour optimiser
        le suivi des étudiants en alternance et faciliter les interactions entre eux, leurs tuteurs, et la coordinatrice
        pédagogique. Ce site offre des fonctionnalités centralisées et intuitives qui permettent aux utilisateurs
        d'accéder facilement aux informations et outils nécessaires pour assurer un suivi optimal.
        Les étudiants disposent d’un profil dédié pour suivre leur progression en entreprise, consulter leurs évaluations, et accéder
        aux retours de leurs tuteurs. Les tuteurs, de leur côté, bénéficient d'un espace où ils peuvent remplir les bilans, noter les progrès, et signaler
        toute difficulté ou besoin spécifique à la coordinatrice. Un système d’alertes automatiques est également intégré pour rappeler
        les échéances importantes et les documents à compléter, facilitant ainsi le respect des délais et la rigueur du suivi. La plateforme intègre
        également une section de paramétrage pour les administrateurs, permettant la configuration des classes, spécialités, et autres éléments fondamentaux.
        En simplifiant les processus administratifs et en centralisant les informations, cette solution moderne offre une gestion fluide et efficace du tutorat,
        remplaçant les outils traditionnels par un espace interactif et sécurisé, parfaitement adapté aux besoins du tutorat en entreprise.
